list类型

//list中增加元素
groovy:000> []
===> []
groovy:000> [1,2,3]
===> [1, 2, 3]
groovy:000> [1,2,3] + 4
===> [1, 2, 3, 4]
groovy:000> [1,2,3] + [5,6,7]
===> [1, 2, 3, 5, 6, 7]
groovy:000> [1,2,3] + [5,6,7] << 8
===> [1, 2, 3, 5, 6, 7, 8]


//list中元素去重
groovy:000> [1,2,3,3,4,5,5,6,6].unique();
===> [1, 2, 3, 4, 5, 6]

//将元素按照一定的符号连接
groovy:000> [1,2,3,3,4,5,5,6,6].join('-');
===> 1-2-3-3-4-5-5-6-6


//求和，最大值，最小值
groovy:000> [1,2,3,4,5].max();
===> 5
groovy:000> [1,2,3,4,5].min();
===> 1
groovy:000> [1,2,3,4,5].sum();
===> 15

//元素个数统计
groovy:000> [1,2,3,3,4,5].count(3);
===> 2

//反转和排序
groovy:000> [2,3,1,5,4,8,6].sort();
===> [1, 2, 3, 4, 5, 6, 8]
groovy:000> [1,2,3,3,4,5].reverse();
===> [5, 4, 3, 3, 2, 1]








